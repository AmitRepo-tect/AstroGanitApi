package com.astroganit.api.model.muhurat;

public class MuhuratModel {
	private String day;
	private String tithi;
	private String var;
	private String nakshtra;
	private String time;
	public String getDay() {
		return day;
	}
	public void setDay(String day) {
		this.day = day;
	}
	public String getTithi() {
		return tithi;
	}
	public void setTithi(String tithi) {
		this.tithi = tithi;
	}
	public String getVar() {
		return var;
	}
	public void setVar(String var) {
		this.var = var;
	}
	public String getNakshtra() {
		return nakshtra;
	}
	public void setNakshtra(String nakshtra) {
		this.nakshtra = nakshtra;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	
}
